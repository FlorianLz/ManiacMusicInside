                <div class="post-body">
                    <p><i>On évoque dans cet article un groupe nommé « BIGBANG ». C’est le groupe le plus connu aujourd’hui, il a rencontré un franc succès dans les années plus « folles » de la k-pop avant qu’elle ne se base davantage sur la culture pop américaine. BIGBANG est un groupe composé de quatre membres. Ce sont les figures montantes du label qui les entraîne, le YG Entertainment. Les membres s’appellent G-Dragon qui est le leader, T.O .P, Daesung et Taeyang. Officiellement, à la suite d’événements tragiques, l’agence a cessé tout contrat avec Seungri qui était le cinquième membre du groupe.</i></p>
                    <p> <img src="img/articles/bigbang1.jpg" alt="..." class="img-fluid"></p>
                    <p>Ce succès démarre en 2006 mais prend de l’ampleur avec le titre « Lies » qui sortira en 2007. Ils se font surnommer « les King de la K-pop ». Ils doivent leur importance à leur style musical reconnaissable entre tous. Ce sont des hommes énergiques, polyvalents, notamment avec leur capacité à rapper et chanter indépendamment. On compte trois rappeurs dans le groupe et c’est exceptionnel pour des idoles d’avoir un groupe composé davantage de rappeurs que de vocalistes. G- Dragon, T.O.P et Taeyang sont les rappeurs du groupe. Daesung est aujourd’hui le dernier vocaliste.</p>
                    
                    <p>L’extravagance des membres se transcrit par leurs capacités vocales mais également par leur style vestimentaire. Personne ne nie reconnaître les membres et ce juste par leur habillement.</p>
                    
                    <p>Parlons musique, parlons succès. Après Lies, c’est Fantastic Baby, sortie en 2014 sur le net qui fait parler d’elle. On fait face à une audience de 100.000.000 de visionnages pour la première fois dans le monde de la K-pop sur YouTube. Ils sont aujourd’hui à la tête de 6 albums studios et 8 albums live.</p>
                    
                    <p>Après Fantastic Baby, c’est le titre « BangBangBang » qui fait parler de lui en 2015 et atteint rapidement les 200.000.000 de vues. Même dix ans après, le groupe n’a de cesse d’imposer son extravagance et son énergie dans la pop coréenne.</p>
                    
                    <p>Le groupe ne s’arrête pas là et les membres font des solos en parallèle du groupe. Le plus connu est G-Dragon, une star internationale dans le monde du rap et de la mode. De son côté, il possède une discographie, qui démarre en 2009, importante rien que pour lui en annexe de son groupe, avec treize singles qui forment trois albums studios. « Coup d’Etat », « Crayon » et « One of a kind » sont trois solos avec un grand retentissement auprès de ses fans. C’est sans compter des carrières en duo entre G-Dragon et T.O.P.</p>
                    
                    <p>Taeyang se lancera lui aussi dans une carrière en parallèle de BIGBANG à partir de 2007. Il compte moins de singles que G-Dragon mais sa capacité à chanter au détriment de son talent de rappeur se révèle être un atout auprès de ses fans notamment dans la chanson « Eyes Nose Lips ». Il possède une voix douce, très calme et légère qui a séduit les oreilles d’un large public en 2014.</p>
                    
                    <p>Continuons avec Daesung. Sa première chanson solo, Try Smiling, sort cette même année. Elle est présente sur le troisième single de BIGBANG. Il a sorti deux albums studios et a participé à des comédies musicales telles que « Cats » et « D-Lite ».</p>
                    
                    <p>Enfin, le dernier membre, Seungri, n’aura sorti de son côté que trois singles, « The Next Day » en 2006, « String Baby » en 2008 avec G-Dragon et « Gotta talk to U » en 2013. Il a participé à des comédies musicales telles que « Sonagi » et « Shouting! ».</p>
                    
                    <p>Les coréens se connaissent depuis l’enfance. Quoi de plus touchant ? Leur amitié les a menés jusqu’en haut de l’échelle de popularité. Une bonne entente, des belles voix, un style unique, une musicalité haute en couleurs… Tout pour bouleverser en partie un jeune public.</p>
