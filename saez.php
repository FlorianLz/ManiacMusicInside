                <h1>Saez<a href="#"><i class="fa fa-bookmark-o"></i></a></h1>
                <div class="post-footer d-flex align-items-center flex-column flex-sm-row"><a href="#" class="author d-flex align-items-center flex-wrap">
                    <div class="avatar"><img src="img/Alex.JPG" alt="..." class="img-fluid"></div>
                    <div class="title"><span>Alexandre Hoarau</span></div></a>
                  <div class="d-flex align-items-center flex-wrap">       
                    <div class="date"><i class="icon-clock"></i> 18 Février 2019</div>
                  </div>
                </div>
                <div class="post-body">
                    <p><i>De son titre Jeune et Con à son bras de fer contre la société française ces dernières
        années, en passant par les attentats de Charlie Hebdo 2015 qui marqua grandement
            Damien et en attendant son prochain album A Dieu qui sortira le <u>1 Février prochain</u>. Nous
        allons nous plonger dans ses 20 ans de carrière, de ses mantras à ses coups de gueule.</i></p>
                    <p> <img src="img/articles/sa1.jpg" alt="..." class="img-fluid" style="float:right;width:340px;"></p>
                    <p>C’est en 1999 que Damien SAEZ se fait un nom dans le paysage musical français.<br>
                    Le natif de Saint-Jean-de-Luz qui a passé la majeure partie de son enfance à Dijon décide
                    de monter à la capitale pour vivre de son rêve, vivre de sa musique. Il passa par un grand
                    nom de la musique : William SHELLER, pour avoir un entretien chez Universal. Ce dernier
                    accrocha au projet du jeune Damien, il obtient un rendez-vous où il présenta notamment
                    Roméo et Juliette ce qui lui permettra de décrocher un contrat avec cette grande
                    entreprise du disque.<br>
                    Tout s’enchaîne très vite, car Damien avait déjà quasiment écrit tous ses textes
                    durant son adolescence. Son première album <u>Jours Etranges</u> fut un succès commercial,
                    double disque. Avec une accroche pareille « puisqu’on est jeune et con, puisqu’ils sont
                    vieux et fous » il ne passa pas inaperçue. Déjà à l’époque il avait un regard amer envers la
                    société qui laissait « les hommes crever sous les ponts ».<br>
                    Dans l’ensemble, c’est un album assez bon sans grande innovation instrumentale
                    mais avec déjà des textes profonds malgré la jeunesse. Une volonté notable de Damien
                    est de ne pas avoir le moindre piano alors que c’est l’instrument avec lequel il a fait le
                    conservatoire dans sa jeunesse.</p>
                    
                    <p> <img src="img/articles/sa2.jpg" alt="..." class="img-fluid" style="float:right;width:340px;"></p>
                    <p>En 2002, à la suite de l’attentant de 2001 et malgré la compassion pour les
                    victimes, il se manifesta chez lui un ras le bol considérable et sort un double album <u>God
                    Blesse.</u> « Je voulais ne vous offrir que la cime mais on ne le sépare jamais de son
                    châtiment. ». Pourquoi ce sentiment de déplaisance ? Le fait que cela touche l’Oncle Sam
                    avec tout l’engouement qui y suivit, la succession de reportages et de témoignages. A
                    contrario, lorsque la misère et les atrocités se déroulent dans des pays d’Afrique, on n’en
                    en entend pas parler. D’où les « God Blesse America » qu’il scande dans son album et lors
                    de ses concerts.<br>
                    Cet album ne comporte pas moins de 9 titres instrumentaux (thèmes) et si le piano était
                    absent sur l’album précédent, ici il revient plus fort que jamais. Il y a une réelle cassure
                        souhaiter dans <u>God Blesse</u>. Beaucoup plus mélodieux et travaillé et avec des titres
                    beaucoup plus longs. Certains même dépassent les 7 minutes, ce qui sort totalement de la
                    standardisation en vigueur.<br>
                    Cependant ce deuxième album s’écoule moitié moins que Jours Etranges. A l’instar de ces
                    ventes, Damien se crée des fans beaucoup plus fidèles, qui viennent en concert, donnent
                    de la voix et qui sont prêts à le suivre dans ses aventures. Cet album marque un tournant
                    majeur de sa carrière, il passe du statut de chanteur adolescent à un auteur-compositeur
                    bien plus mature que son âge le laisse paraître.
                    Des chansons comme J’veux qu’on baise sur ma tombe, A ton nom ou encore Saint
                    Pétersbourg sont marquants et resteront des classiques interprétés en concert même 17
                    ans après.<br>
                    Lorsqu’il émit l’idée de sortir un album à sa maison de production, cette nouvelle aventure
                    double au prix d’un, Universal ne voyait pas ça comme quelques choses de rentable.
                    S’ensuivent pleins de désaccords divers et variés notamment sur la promo que Saez ne
                    voulait pas faire. Il décide de claquer la porte pour ne pas finir en sonnerie de téléphone.</p>
                    
                    <p> <img src="img/articles/sa3.jpg" alt="..." class="img-fluid" style="float:left;width:340px;"></p>
                    <p>Les 3 années suivantes, il travailla avec un nouveau label (Cinq7) sur son prochain
                    album : <u>Debbie</u>. Beaucoup de cuivres sont présents, une nouvelle fois Damien surprend et
                    change totalement d’orientation. Un dépaysement total comme un besoin de prouver à
                    son ancienne maison de disque qu’il est bien là et qu’il continue de grandir. Des textes de
                    plus en plus léchés et travaillés en concordance de la mélodie. Il se réinvente et poursuit
                    son petit bonhomme de chemin.<br>
                    De plus, la chanson <i>Marie ou Marilyn</i> est la B.O du film Femme Fatale.<br><br>
                    Petite astuce : écoutez <i>Tu y crois</i> jusqu’à la fin 12 minutes cela peut paraître
                    étrangement mais un titre caché y est exposé.</p>
                    
                    <p> <img src="img/articles/sa4.jpg" alt="..." class="img-fluid" style="float:right;width:340px;"></p>
                    <p>En 2007, après plusieurs années passées aux côtés de sa compagne, Katarzyna
                    (Kasia) Krynska décide de se séparer de Saez. Une rupture brutale à laquelle il ne
                    s’attendait pas. Le réflexe qu’il a eu fut de faire la seule chose pour laquelle il est
                        réellement doué : l’écriture. Et c’est ainsi que son triple album <u>Varsovie</u> vit le jour. En deux
                    semaines seulement, il a écrit et enregistré ce triptyque. À l’exception que quelques
                    chansons l’intégralité d’entre elles ont été captées en une prise. Les paroles sont
                    beaucoup plus noires, la composition musicale et les riffs sont plus simples, les
                    instruments dominants sont majoritairement la guitare et le piano.<br>
                    Ceci entraînera des concerts acoustiques vraiment incroyables à voir et entendre.
                    Ses mantras qu’il manifeste au long des 29 chansons se concluent par <i>Kasia</i> comme un
                    aurevoir à sa bienaimée. C’est un voyage de Varsovie en passant par New York et la
                    séparation qui fut si brutale était peut-être prévisible comme il le dit « <i>Tu voulais aller
                    parallèle quand j’allais qu’à l’oblique</i></p>
                    
                    <p> <img src="img/articles/sa5.jpg" alt="..." class="img-fluid" style="float:left;width:340px;"></p>
                    <p>Après deux ans passés sans carte bleue à cause de soucis avec la banque, Saez
                    revient en 2009 avec un album frappant : « <u>J’accuse</u> ». Un constat de la société qu’il a
                    perçu en se baladant lui-même dans les magasins. Ce nouvel opus va faire parler de lui
                    notamment à cause de sa pochette : une femme nue dans un caddie, dans le coin droit
                    supérieure il est inscrit « <u>J’accuse</u> » qui fait écho forcément à Emile Zola près d’un siècle
                    plus tard. La photo est signée Jean-Baptiste Mondino, la femme dans le caddie n’est nulle
                    autre que la mannequin Jennifer Lamiraqui qui se trouve être la compagne du chanteur.<br>
                    L’affiche de promo a été interdite dans le couloir des métros par l'Autorité de
                    Régularisation Professionnelle de la Publicité parce qu’ils trouvent l’image dégradante pour
                    la femme. Ce qui va le faire monter dans les tours et faire plusieurs apparitions à la
                    télévision pour manifester son mécontentement. Il passe notamment par l’émission « Ce
                    soir ou jamais » où son message est très clair : c’est simple la censure gratuite. Il explique
                    que lorsque c’est pour « vendre des soutifs » il n’y a pas de soucis, on laisse passer. De
                    plus, à la suite de la censure de son affiche, le natif de Saint-Jean-de-Luz a réalisé une
                    seconde affiche où il est inscrit « La photo initialement prévue pour cet affichage a été
                    interdite dans les couloirs de nos métros ». Celle-ci fut également interdite, c’est pour cela
                    qu’il est sorti du silence car ici il n’y a rien de « choquant » et qu’il ne comprend pas
                    pourquoi il n’a pas eu le droit au même traitement que les autres.<br>
                    Côté musique « J’accuse » est très rock, les guitares électriques sont de sortie,
                    accompagnées d’une batterie. La particularité du premier morceau est acapela, les textes
                    sont beaucoup moins profonds et la mélodie moins recherchée mais le message qu’il y
                    transmet est plus important que la forme. Il veut souligner les problèmes de la société et
                    les méfaits du capitalisme « <i>Production, Blé, Pognon !</i> ». Le constat dans le monde où se

                    retrouvent les gamins dans <i>Les cours des lycées</i>. Cependant, l’album finit sur une touche
                    très légère avec <i>tricycle jaune</i>, appréciable toujours sur le même thème mais avec un
                    angle, une vision différente qui rafraîchit et fait réfléchir différemment.<br><br>
                    Il est nommé en 2009 dans la catégorie album pop rock aux Victoires de la
                    Musique. Il participe même à la cérémonie en direct avec une apparition de plus de 10
                    minutes… Il devait en réaliser une de 3 minutes… Le réalisateur et tous les techniciens
                    étaient fous autour de la scène. A la fin de sa représentation, Naguy (le présentateur) est
                    totalement surpris par ce qu’il a vu et a tenu à remercier Damien Saez mais aussi les
                    personnes qui se sont occupées des jeux de lumière.</p>
                    
                    <p> <img src="img/articles/sa6.jpg" alt="..." class="img-fluid" style="float:right;width:340px;"></p>
                    <p>On ne le reverra plus avant 2012 où il reviendra avec un nouveau triple album :
                    <u>Messine</u>. Encore une ville comme 5 ans auparavant, il aime employer des noms de villes
                    ou encore des prénoms dans ses chansons/titres car « ça évoque toujours quelque chose,
                    qu’on la connaisse ou qu’on ne la connaisse pas […] c’est simple et ça parle ». Messine est
                    considéré comme le meilleur album qu’il ait pu faire. Tout un orchestre rien que pour lui, la
                    composition est juste incroyable notamment sur <i>Marie, Bouteille à la mer ou Les
                    Magnifiques</i>. Et l’écriture nécessite plusieurs passages pour comprendre toutes les
                    subtilités, elle est parfois même au second plan tellement l’orchestre prend de la place.
                    La pochette un patchwork assez intriguant, comportant une multitude de photos obscures.
                    A l’intérieur de la pochette, on retrouve un remerciement à Nelly et Bruno. Nelly est
                    la mère de son beau-père qui lui a donné le goût de la lecture et surtout de Rimbaud.
                    Quant à Bruno il était professeur de Jazz à Chatillon sur Seine, un lieu où il a passé
                    quelque temps dans sa jeunesse chez Nelly. Il lui a dédié le dernier titre du dernier CD, un
                    remerciement pur, touchant et désintéressant. « <i>Tu m’as sauvé la peau</i></p>
                    
                    <p> <img src="img/articles/sa7.jpg" alt="..." class="img-fluid" style="float:left;width:340px;"></p>
                    <p>Quelques mois plus tard, un énième album sort dans les bacs : <u>Miami</u> (on notera,
                    encore une ville). Un album rock comme si finalement il y revient toujours. Beaucoup plus
                    modeste que le précédent, mais avec une pochette qui va encore faire parler mais Saez ne
                    cherchera pas à avoir gain de cause et n’en parlera même pas publiquement. L’affiche fut
                    interdite diffusion comme celle de <u>J’accuse</u>. L’album sonne très ancien comme un retour
                    aux sources et finit par <i>Que sont-elles devenues ?</i> où la nostalgie fait place. On remarque
                    même un écho à son premier album et la chanson <i>Amandine « L’héroïne »</i> comme un
                    sentiment que sa première copine l’ait oublié.</p>
                    
                    <p> <img src="img/articles/sa8.jpg" alt="..." class="img-fluid" style="float:right; width:340px;"></p>
                    <p>Un évènement atroce choqua le monde entier mais aussi Saez. Charlie Hebdo, le
                    Bataclan (où il s’est produit à de nombreuses reprises) … Il publie quelques temps après
                    un clip noir et blanc le mettant en scène sur une plage, plantant des fleurs dans le sable.
                    Ce qui annonce sa prochaine aventure : <u>Le Manifeste</u>. Un projet ambitieux de juillet 2017
                    à juillet 2018, avec l’ouverture d’un site culturecontrecultutre où l’on peut s’abonner et
                    ainsi profiter de sa discographie en streaming, retrouver des courts métrages qui seront
                    également diffusés en concert mais aussi des textes que Damien postera de temps en
                    temps.<br>
                    Tout cela est accompagné par l’annonce du nouvel album L’oiseau liberté. Un album
                    parlant des attentats « à l’heure des guerres des champs d’horreur, faire de la terre des
                    champs de fleurs ». Il est joint une pochette avec un prélude du prochain album Lulu qui
                    sortira 1 an plus tard, comportant 3 chansons.
                    Des morceaux lourds de sens et profonds. Du troc de la violence à la culture, de
                    l’oppression à la liberté.<br>
                    Un album comportant seulement 7 chansons, pour la première partie de son manifeste.</p>
                    
                    <p> <img src="img/articles/sa9.jpg" alt="..." class="img-fluid" style="float:left;width:340px;"></p>
                    <p>La partie 2 intervient en 2017 : Lulu « des mots d’amour contre un empire ». Après
                    Varsovie, Messina un nouveau triple album signé Damien Saez. Cette fois ci un album bien
                    complet, des morceaux bien rock comme Bonnie ou encore La Rue de la Soif, des
                    musiques « thèmes » aux nombres de 5. On ressent l’influence de Renaud dans beaucoup
                    de ses nouvelles chansons comme Ma Gueule, P’tit Bout D’Paradis ou encore Lulu qui fait
                    un écho à Manu. Toujours un regard sur la société, l’amour et la religion.</p>

                    <p>En 2018 le rockeur est de retour avec #Humanité une pochette atypique, on
                    commence à avoir l’habitude avec lui. Une femme qui se filme, le téléphone dans une
                    main et l’autre avec une arme pointée sur sa tempe et un sein apparent. Une nouvelle fois
                    pour manifester la pornographie ambiante sur les réseaux sociaux et ça sera l’axe majeur
                    de ce nouvel opus. Sauf Attentat qui s’inspire d’une chanson de Mano Negra et Burqa où il
                    parle de religion d’une façon totalement décalée et sans jugement ; on connait son
                    attachement à la mixité culturelle.
                    </p>

                    <p>Damien Saez a un univers bien à lui, inimitable, on ne peut prévoir la trajectoire
                    qu’il donne à son travail. Un écorché qui ne cesse de crier son amour au monde en
                    rappelant qu’on est Menacé mais libre.</p>

