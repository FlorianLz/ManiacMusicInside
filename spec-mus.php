                <div class="post-body">
                    <p><i>Dans les années 1970, quand les jeux vidéo commencèrent à être source de divertissement, la musique commença à être stockée de manière analogique sur des supports comme les cassettes audio et les disques phonographiques. Toute l’histoire de la musique vidéoludique a été conditionnée par les capacités de stockage des jeux. Capacités très limitées dans les années 70 et 80, impossible d’utiliser un orchestre symphonique ni même un instrument acoustique.</i></p>
                    
                    <p> <img src="img/articles/pacman.jpg" alt="..." class="img-fluid" style="float:right; width:340px;margin-left:10px;"></p>
                    <p><center><b>La musique et les jeux</b></center><br>
                    La musique de jeux vidéo n’a pas un style particulier, mais elle reste soumise à certaines
                    contraintes importantes. De plus il n’existe pas qu’un seul type de jeux.
                    Entre un jeu de course, un jeu de stratégie, un jeu de guerre et un jeu de rôle, ce ne sont pas
                    les mêmes attentes ni les mêmes contraintes en matière de musique.<br><br>  

                    Il fallait auparavant suivre le rythme du jeu, appuyer sur les bonnes touches au bon moment,
                    neutraliser les obstacles qui s’accumulaient sur un rythme précis, et la musique donnait le
                    tempo au joueur… maintenant, vous avez généralement tout loisir de vivre l’aventure à votre
                    propre rythme.<br><br>

                    Foncer dans le tas comme un bourrin lors d’un combat ou utiliser votre environnement d’une
                    manière plus stratégique, explorer chaque recoin du territoire que vous parcourez ou filer tout
                    droit vers votre prochaine destination etc… Et c’est là ce qui entraîne une des différences
                    majeures avec la musique dans les films.<br><br>

                    Dans un film, le réalisateur, le monteur et le compositeur peuvent faire coïncider au millième
                    de seconde près la musique et les images, pas dans un jeu… La recherche de cohérence et
                    d’homogénéité est donc cruciale dans la musique de jeux vidéo par exemple :
                    Une situation anxiogène sur fond de tremolo de violons, puis la menace se dévoile
                    rapidement, rythmée par de grands coups de percussions et des traits de contrebasse, le héros
                    surgit accompagné par un thème plein d’emphase.<br><br>

                    En effet, un morceau avec un thème sombre et martial qui surgit au moment où vous cueillez
                    une fleur n’est pas terrible pour l’immersion.<br><br>

                    C’est là un des défis majeurs des compositeurs de musiques de jeux vidéo ; créer des
                    musiques qui peuvent s’emboîter à n’importe quel moment, mais avec des contrastes qui
                    permettent de marquer les changements de situation.</p>

