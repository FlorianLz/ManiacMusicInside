                <div class="post-body">
                    <p><i>Interprétée par Pink Floyd, la musique Wish You Were Here a été écrite par Roger Waters et composée par David Gilmour. Elle est parue sur l’album du même nom le 12 septembre 1975 en mémoire de l’ancien leader Syd Barrett.</i></p>
                    
                    <p> <img src="img/articles/wishyouwerehere.png" alt="..." class="img-fluid" style="float:right; width:340px"></p>
                    <p><center><b>Un peu d’histoire,</b><br></center>
                    Issu de la scène underground londonienne, le groupe de rock Pink Floyd est mené par un guitariste et parolier de génie : Syd Barrett, qui s’accompagnera de trois amis : Nick Mason, Richard Wright et Roger Waters.<br>
                    Le groupe commence à connaître un certain succès commercial au milieu des années 60. Mais malheureusement, Syd Barrett devient incontrôlable et sa consommation massive de LSD l'oblige à sortir du groupe en 1968.<br>
                    Le groupe continuera sans son leader, vite remplacé par son amis guitariste et chanteur David Gilmour, Barrett entamera alors une carrière solo concluante avant de prendre sa retraite.</p>
                    
                    <p><center><b>La naissance du morceau,</b><br></center>
                    C’est en 1975 que Waters, Gilmour, Wright et Mason se retrouvent aux mythiques studios d’Abbey Road pour enregistrer leur prochain opus : Wish You Were Here, contenant une seul Pièce maîtresse dans un nouvel album-concept : le long morceau en neuf parties "Shine on you crazy diamond", album qu’ils souhaitent consacrer à leur ancien leader.<br>

                    Syd Barrett n’est pas seulement le sujet de la chanson, il est le thème principal de l’album<br>
                    En effet en 2006, l'ancien leader meurt. Et la chanson Wish You Were Here de l'album est dédié à celui qui restera à jamais lié à Pink Floyd.<br>
                    Cette œuvre raconte le manque et l'absence qu’il a créé au sein du groupe mis en musique tel que l’ont ressenti les membres, mais aussi les tensions qui animaient les relations entre le chanteur et guitariste David Gilmour et Roger Waters auteur des textes.<br>

                    Il s’agit de l’une des chansons les plus connues de Pink Floyd et en effet,<br>
                    Les admirables 44 minutes (pour 5 titres seulement) font de Wish You Were Here un album mythique et un des sommets du rock progressif et du rock tout court.<br>
                    Un disque pour le moins emblématique et a l‘atmosphère glaciale et sombre qui traumatisera encore une fois les fans du groupe qui à l'époque n’étaient pas encore remis du succès monumental du précédent album, The Dark Side Of The Moon de 1973.<br><br>

                    <center><b>Composition,</b><br></center>
                    Le groupe construit la chanson de manière particulière avec plusieurs niveaux de couches et avec notamment une alternance entre musique acoustique et électrique et mono/stéréo. David Gilmour interprète l’introduction avec une guitare acoustique à 12 cordes sur un solo qui donne l'impression d’une vieille radio à lampes (ce qui est le cas), les effets de changement de station passant par un enregistrement de la Symphonie n°4 de Tchaikovsky) s'arrêtent sur celle jouant « Wish You Were Here ».<br>
                    La radio utilisée est celle de la voiture de David Gilmour. À la fin de la chanson, le bruit du vent apparaît, le même que dans « One of These Days ».<br><br>




                    <center><b>Succès,</b><br></center>
                    La chanson Wish You Were Here va connaître un important succès auprès du public européen et nord-américain dès sa sortie en 1975. Elle s’installera solidement aux premières places des charts des deux côtés de l’Atlantique et trouvera au fil du temps sa place dans plus de quinze millions de foyers à travers le monde.<br>
                    Aujourd’hui encore, il s’agit de l’une des chansons les plus connues de Pink Floyd. Le titre sera par ailleurs repris de nombreuses fois par des groupes et musiciens comme Sparklehorse, The Flaming Lips, Velvet Revolver ou encore Wyclef Jean.<br><br>
                    </p>
                    
                    
                    
                    
